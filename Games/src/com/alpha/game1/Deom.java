package com.alpha.game1;

public class Deom {
    private String name;
    private int age;

    public Deom() {
    }

    public Deom(String name, int age) {
        this.name = name;
        this.age = age;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
